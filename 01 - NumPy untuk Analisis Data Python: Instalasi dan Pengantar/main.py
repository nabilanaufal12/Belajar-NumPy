import numpy as np

# Membuat *Array* NumPy dan *List* Python
## Membuat *array* NumPy
a = np.array([1, 2, 3, 4])
print(a)

## Membuat *list* Python
b = [1, 2, 3, 4]
print(b)

# Perbedaan dalam Operasi Matematika
## NumPy Array
a_plus_one = a + 1
print(a_plus_one)

## Python List
b_plus_one = b + [1]
print(b_plus_one)